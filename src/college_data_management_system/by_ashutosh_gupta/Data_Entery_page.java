        private void updateUserDetails() {
                try {
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/colledge_database",
                                        "root", "");
                        String query = "UPDATE login_data SET Gender=?, Father_Name=?, `Mother's_Name`=?, Address=?, Marks_12=?, Marks_10=?, Adhaar_number=?, profile_locked=1 WHERE User_ID=?";
                        PreparedStatement pst = conn.prepareStatement(query);

                        // --- Gender conversion ---
                        String genderValue = "O";
                        String comboGender = CB.getSelectedItem().toString();
                        if (comboGender.equalsIgnoreCase("Male"))
                                genderValue = "M";
                        else if (comboGender.equalsIgnoreCase("Female"))
                                genderValue = "F";
                        // else remains "O"

                        pst.setString(1, genderValue); // Gender as 'M', 'F', or 'O'
                        pst.setString(2, TF1.getText()); // Father's Name
                        pst.setString(3, TF2.getText()); // Mother's Name
                        pst.setString(4, TF3.getText()); // Address
                        pst.setString(5, TF4.getText()); // 12th Marks
                        pst.setString(6, TF5.getText()); // 10th Marks
                        pst.setString(7, TF6.getText()); // Adhaar Number
                        pst.setString(8, loggedInUserId); // User ID

                        int rs = pst.executeUpdate();
                        if (rs > 0) {
                                JOptionPane.showMessageDialog(this, "Details updated successfully!");
                        } else {
                                JOptionPane.showMessageDialog(this, "Update failed.");
                        }
                        pst.close();
                        conn.close();
                } catch (Exception e) {
                        e.printStackTrace();
                        JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
                }
        }

