        private boolean validate_login(String username, String password) {
                try {
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/colledge_database",
                                        "root", "");
                        PreparedStatement pst = conn
                                        .prepareStatement("Select * from login_data where User_ID=? and Password=?");
                        pst.setString(1, username);
                        pst.setString(2, password);
                        ResultSet rs = pst.executeQuery();
                        if (rs.next())
                                return true;
                        else
                                return false;
                } catch (Exception e) {
                        e.printStackTrace();
                        return false;
                }
        }

