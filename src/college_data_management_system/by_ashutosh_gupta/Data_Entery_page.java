        private void loadUserData() {
                try {
                        Class.forName("com.mysql.cj.jdbc.Driver");
                        Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/colledge_database",
                                        "root", "");
                        String query = "SELECT * FROM login_data WHERE User_ID = ?";
                        PreparedStatement pst = conn.prepareStatement(query);
                        pst.setString(1, loggedInUserId);
                        ResultSet rs = pst.executeQuery();
                        if (rs.next()) {
                                jLabel9.setText(rs.getString("User_ID"));
                                jLabel10.setText(rs.getString("Name"));
                                jLabel11.setText(rs.getString("Phone_No"));
                                jLabel12.setText(rs.getString("Mail_ID"));

                                // Gender conversion
                                String gender = rs.getString("Gender");
                                if (gender != null) {
                                        if (gender.equalsIgnoreCase("M"))
                                                CB.setSelectedItem("Male");
                                        else if (gender.equalsIgnoreCase("F"))
                                                CB.setSelectedItem("Female");
                                        else
                                                CB.setSelectedItem("Other");
                                }
                                TF1.setText(rs.getString("Father_Name"));
                                TF2.setText(rs.getString("Mother's_Name"));
                                TF3.setText(rs.getString("Address"));
                                TF4.setText(rs.getString("Marks_12"));
                                TF5.setText(rs.getString("Marks_10"));
                                TF6.setText(rs.getString("Adhaar_number"));

                                // ---------- LOCK LOGIC HERE ----------
                                boolean isProfileLocked = false;
                                try {
                                        isProfileLocked = rs.getInt("profile_locked") == 1;
                                } catch (Exception ignore) {
                                } // if column not present

                                if (isProfileLocked) {
                                        // Lock everything, show as completed
                                        CB.setEnabled(false);
                                        TF1.setEnabled(false);
                                        TF2.setEnabled(false);
                                        TF3.setEnabled(false);
                                        TF4.setEnabled(false);
                                        TF5.setEnabled(false);
                                        TF6.setEnabled(false);
                                        B1.setEnabled(false);
                                        B2.setEnabled(false);
                                        B3.setEnabled(false);
                                        CB1.setEnabled(false);
                                        CB1.setSelected(true);
                                        B4.setEnabled(true);
                                        B5.setEnabled(false);
                                } else {
                                        // (Optional) You can also check all fields not null to determine if locked,
                                        // but using a dedicated column is cleaner!
                                }
                        }
                        rs.close();
                        pst.close();
                        conn.close();
                } catch (Exception e) {
                        e.printStackTrace();
                }
        }

