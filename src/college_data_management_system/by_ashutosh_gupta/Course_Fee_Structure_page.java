    private void updateUserDetails() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection(
                    "jdbc:mysql://localhost:3306/colledge_database", "root", "");

            // Update only for this user
            String query = "UPDATE login_data SET course=?, Fee=?, fee_lock=1 WHERE User_ID=?";
            PreparedStatement pst = conn.prepareStatement(query);

            // Get selected course (combo box value, for DB field 'course')
            String course = (CB.getSelectedItem() != null) ? CB.getSelectedItem().toString() : "";

            // Get the final total fee from L10 label (not the editable field TF)
            int totalFee = 0;
            try {
                totalFee = Integer.parseInt(L10.getText());
            } catch (Exception ex) {
                // fallback: try TF field, if needed
                try {
                    totalFee = Integer.parseInt(TF.getText());
                } catch (Exception ignore) {
                }
            }

            pst.setString(1, course); // course name
            pst.setInt(2, totalFee); // final total fee
            pst.setString(3, loggedInUserId); // update only current user

            int rs = pst.executeUpdate();
            if (rs > 0) {
                JOptionPane.showMessageDialog(this, "Course & Fee submitted and locked!"); // success
            } else {
                JOptionPane.showMessageDialog(this, "Update failed! User not found?");
            }

            pst.close();
            conn.close();

            // Lock the page after update for this user
            CB.setEnabled(false);
            TF.setEnabled(false);
            B1.setEnabled(false);
            B2.setEnabled(false);
            B3.setEnabled(false);
            CB1.setEnabled(false);
            CB1.setSelected(true);
            B4.setEnabled(false);
            B5.setEnabled(false);

        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error: " + e.getMessage());
        }
    }

