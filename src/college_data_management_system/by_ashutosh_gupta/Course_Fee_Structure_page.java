    private void loadFeeData() {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/colledge_database", "root", "");
            String query = "SELECT * FROM login_data WHERE User_ID = ?";
            PreparedStatement pst = conn.prepareStatement(query);
            pst.setString(1, loggedInUserId);
            ResultSet rs = pst.executeQuery();
            if (rs.next()) {
                // Set user info at the top
                jLabel8.setText(rs.getString("User_ID"));
                jLabel9.setText(rs.getString("Name"));

                String course = rs.getString("course");
                int fee = rs.getInt("Fee");
                int feeLock = rs.getInt("fee_lock");

                if (course != null && !course.trim().isEmpty()) {
                    CB.setSelectedItem(course);
                }

                // Always set the full fee structure as per selected course (from your dropdown
                // logic)
                setFeeStructureByCourse(course);

                // Set the final total amount as in DB
                L10.setText(String.valueOf(fee));

                // Calculate and display donation (TF): Fee - sum of other components
                int b = Integer.parseInt(L2.getText());
                int c = Integer.parseInt(L3.getText());
                int d = Integer.parseInt(L4.getText());
                int e = Integer.parseInt(L5.getText());
                int f_ = Integer.parseInt(L6.getText());
                int g = Integer.parseInt(L7.getText());
                int h = Integer.parseInt(L8.getText());
                int i = Integer.parseInt(L9.getText());
                int sum = fee; // as from DB
                int k = sum - (b + c + d + e + f_ + g + h + i);
                TF.setText(String.valueOf(k));

                if (feeLock == 1) {
                    // Lock everything (except B4, which must be enabled for payment)
                    CB.setEnabled(false);
                    TF.setEnabled(false);
                    B1.setEnabled(false);
                    B2.setEnabled(false);
                    B3.setEnabled(false);
                    CB1.setEnabled(false);
                    CB1.setSelected(true); // show confirmed
                    B4.setEnabled(true); // Payment button
                    B5.setEnabled(false);
                }
            }
            rs.close();
            pst.close();
            conn.close();
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error loading fee data: " + e.getMessage());
        }
    }

